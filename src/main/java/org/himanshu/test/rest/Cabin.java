package org.himanshu.test.rest;

/**
 * This is a POJO for cabin
 * @author Himanshu Gusain
 *
 */
public class Cabin {

	int cabinId;
	String cabinName;
	String cabinDescription;
	/**
	 * @return the cabinId
	 */
	public int getCabinId() {
		return cabinId;
	}
	/**
	 * @param cabinId the cabinId to set
	 */
	public void setCabinId(int cabinId) {
		this.cabinId = cabinId;
	}
	/**
	 * @return the cabinName
	 */
	public String getCabinName() {
		return cabinName;
	}
	/**
	 * @param cabinName the cabinName to set
	 */
	public void setCabinName(String cabinName) {
		this.cabinName = cabinName;
	}
	/**
	 * @return the cabinDescription
	 */
	public String getCabinDescription() {
		return cabinDescription;
	}
	/**
	 * @param cabinDescription the cabinDescription to set
	 */
	public void setCabinDescription(String cabinDescription) {
		this.cabinDescription = cabinDescription;
	}
	
	
}
